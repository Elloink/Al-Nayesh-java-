package com.niit.Entity;

public class Employee_Info {
	
	public String Employee_ID;
	public String Employee_Name;
	public int Employee_Age;
	public String Employee_Fhone;
	public String Employee_Password;
	public String Employee_Permission;
	public String getEmployee_ID() {
		return Employee_ID;
	}
	public void setEmployee_ID(String employee_ID) {
		Employee_ID = employee_ID;
	}
	public String getEmployee_Name() {
		return Employee_Name;
	}
	public void setEmployee_Name(String employee_Name) {
		Employee_Name = employee_Name;
	}
	public int getEmployee_Age() {
		return Employee_Age;
	}
	public void setEmployee_Age(int employee_Age) {
		Employee_Age = employee_Age;
	}
	public String getEmployee_Fhone() {
		return Employee_Fhone;
	}
	public void setEmployee_Fhone(String employee_Fhone) {
		Employee_Fhone = employee_Fhone;
	}
	public String getEmployee_Password() {
		return Employee_Password;
	}
	public void setEmployee_Password(String employee_Password) {
		Employee_Password = employee_Password;
	}
	public String getEmployee_Permission() {
		return Employee_Permission;
	}
	public void setEmployee_Permission(String employee_Permission) {
		Employee_Permission = employee_Permission;
	}
	
}
